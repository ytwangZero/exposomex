                   body = list(PID = PID,
                               CrosPredVarsY = VarsY,
                               CrosPredVarsX = VarsX,
                               CrosPredPredType = PredType,
                               CrosPredVarsSel = VarsSel,
                               CrosPredVarsSelThr = VarsSelThr,
                               CrosPredCovariates = Covariates,
                               CrosPredRsmpMethod = RsmpMethod,
                               CrosPredFolds = Folds,
                               CrosPredRatio = Ratio,
                               CrosPredRepeats = Repeats),
