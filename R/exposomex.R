                    OutPath = "default",
                    VarsY,
                    VarsX = "all.x",
                    PredType = "response",
                    VarsSel = F,
                    VarsSelThr = 0.1,
                    Covariates = "all.c",
                    RsmpMethod = "cv",
                    Folds = 5,
                    Ratio = 0.667,
                    Repeats = 5){
  if(OutPath == "default"){
    OutPath = getwd()
  }

  message("It may take some time. Thanks for your patient waiting!")

  url = paste0(urlhead,'CrosPred')
